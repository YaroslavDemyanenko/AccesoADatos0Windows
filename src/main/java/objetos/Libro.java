package objetos;

public class Libro {
	int id;
	String titulo,autor,genero,descripcion;
	
	
	public Libro(int id, String titulo, String autor, String genero, String descripcion) {
		this.id = id;
		this.titulo = titulo;
		this.autor = autor;
		this.genero = genero;
		this.descripcion = descripcion;
	}
	
	
}
